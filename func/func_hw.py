        print("выберите что вам нужно найти: ")
        print("1. периметр")
        print("2. площадь")
        print("3. выход")

        choice = input("выберите операцию (1/2/3): ")
        if choice == '3':
            print("выход из программы")
            break
        if choice not in ('1', '2', '3'):
             print("неверный ввод, пожалуйста, выберите операцию от 1 до 3")
             continue

        num1 = float(input("введите первое число: "))
        num2 = float(input("введите второе число: "))

        if choice == "1":
            print("периметр: ", (num1 + num2) * 2)
        else:
            print("площадь:", num1 * num2)

