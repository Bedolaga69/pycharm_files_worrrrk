def s(a, b):
    """находит площадь прямоугольника через стороны a и b """
    return a * b

def p(a, b):
    """находит периметр прямоугольника через стороны a и b """
    return 2 * (a + b)

a = float(input("введите первое число: "))
b = float(input("введите второе число: "))

print("площадь = ", s(a, b))
print("периметр = ", p(a, b))

